// Current language
let _currentLang = localStorage.getItem('iact3-lang') || (navigator.language.startsWith('zh') ? 'zh' : 'en');

/**
 * Get translation by key, with optional parameter substitution.
 * Falls back to English, then to the key itself.
 */
function t(key, params = {}) {
    let text = (I18N[_currentLang] && I18N[_currentLang][key]) || I18N.en[key] || key;
    // Replace {param} placeholders
    for (const [k, v] of Object.entries(params)) {
        text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    }
    return text;
}

/** Get current language */
function getLang() {
    return _currentLang;
}

/** Set language and apply to DOM */
function setLang(lang) {
    if (!I18N[lang]) return;
    _currentLang = lang;
    localStorage.setItem('iact3-lang', lang);
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
    applyTranslations();
    // Re-fetch regions with new language
    if (typeof regionSelect !== 'undefined' && regionSelect._loaded) regionSelect.reload();
    // Re-apply dynamic content if on certain pages
    if (typeof updateCredentialsBadge === 'function') updateCredentialsBadge();
    // Re-render editor summaries so "lines" and format labels follow current language
    if (typeof _updateEditorSummary === 'function') {
        _updateEditorSummary('workspace', 'template');
        _updateEditorSummary('workspace', 'config');
    }
    if (currentPage === 'reports' && typeof _reportsData !== 'undefined' && _reportsData.length) renderReportTable();
    // Dashboard: re-render runs, history, and projects (filter options + list content)
    if (currentPage === 'dashboard') {
        if (typeof _updateRunsFilter === 'function') _updateRunsFilter();
        if (typeof _renderRunsTags === 'function') _renderRunsTags();
        if (typeof _updateHistoryFilter === 'function') _updateHistoryFilter();
        if (typeof _renderHistory === 'function') _renderHistory();
        if (typeof loadProjectList === 'function') loadProjectList();
    }
    // Detail: re-render if we have cached run data
    if (currentPage === 'detail' && typeof window._currentRun !== 'undefined' && window._currentRun && typeof renderRunDetail === 'function') {
        renderRunDetail(window._currentRun);
    }
    // Update language switcher buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
}

/** Apply translations to all [data-i18n] elements */
function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const text = t(key);
        if (text) el.textContent = text;
    });
    // Apply data-i18n-html (innerHTML)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.dataset.i18nHtml;
        const html = t(key);
        if (html) el.innerHTML = html;
    });
    // Apply data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        el.placeholder = t(key);
    });
    // Apply data-i18n-title
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.dataset.i18nTitle;
        el.title = t(key);
    });
}
